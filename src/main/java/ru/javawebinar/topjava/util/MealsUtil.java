public class MealsUtil {
    private static List<MealTo> mealsTo;
    private static List<Meal> meals;

    static {
       meals = Arrays.asList(
                new Meal(LocalDateTime.of(2015, Month.MAY, 30, 10, 0), "Завтрак", 500),
                new Meal(LocalDateTime.of(2015, Month.MAY, 30, 13, 0), "Обед", 1000),
                new Meal(LocalDateTime.of(2015, Month.MAY, 30, 20, 0), "Обед", 500),
                new Meal(LocalDateTime.of(2015, Month.MAY, 31, 10, 0), "Завтрак", 1000),
                new Meal(LocalDateTime.of(2015, Month.MAY, 31, 13, 0), "Обед", 500),
                new Meal(LocalDateTime.of(2015, Month.MAY, 31, 20, 0), "Ужин", 510)
        );

    }


    private static List<MealTo> getFiltered(List<Meal> meals,int caloriesPerDay) {
        final Map<LocalDate, Integer> caloriesSumByDate = new HashMap<>();
        meals.forEach(meal -> caloriesSumByDate.merge(meal.getDate(), meal.getCalories(), Integer::sum));


        final List<MealTo> mealsTo = new ArrayList<>();
        meals.forEach(meal -> mealsTo.add(createTo(meal, caloriesSumByDate.get(meal.getDate()) > caloriesPerDay))
        );
        return mealsTo;

    }

    private static MealTo createTo(Meal meal, boolean excess) {
        return new MealTo(meal.getDateTime(), meal.getDescription(), meal.getCalories(), excess);
    }

    public List<MealTo> getMealsTo() {
        return getFiltered(meals,2000);
    }
}